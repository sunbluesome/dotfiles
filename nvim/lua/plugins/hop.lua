-- =============================================================================
-- モーションジャンプ設定 (hop.nvim)
-- =============================================================================
-- hop.nvim は EasyMotion の後継となるモーションプラグインです。
-- 画面上の任意の位置にすばやくジャンプできます。
-- 候補が多い場合は複数文字ラベル（aa, ab, ac...）で対応します。
--
-- 使い方:
--   m           : 単語の先頭にジャンプ（ノーマル/ビジュアル/オペレーターモード）
--
-- 例:
--   m + a       : ラベル "a" の位置にジャンプ
--   m + a + s   : ラベル "as" の位置にジャンプ（候補が多い場合）
--   d + m + a   : ラベル "a" の位置まで削除
--
-- 参考: https://github.com/smoka7/hop.nvim
-- =============================================================================

return {
  "smoka7/hop.nvim",

  -- 遅延読み込み: 使用時に読み込む
  event = "VeryLazy",

  -- プラグイン読み込み後に実行される設定
  opts = {
    -- ラベルに使用する文字（ホームポジション優先）
    keys = "asdfghjklqwertyuiopzxcvbnm",

    -- 大文字小文字を区別しない
    case_insensitive = true,

    -- 複数文字ラベルを使用（候補が多い場合 aa, ab, ac... になる）
    multi_windows = false,

    -- ラベルを大文字で表示
    uppercase_labels = false,

    -- ジャンプ後にジャンプリストに追加
    jump_on_sole_occurrence = true,
  },

  -- キーマッピング
  keys = {
    {
      "m",
      function()
        require("hop").hint_words()
      end,
      mode = { "n", "x", "o" },
      desc = "Hop to word",
    },
  },
}
